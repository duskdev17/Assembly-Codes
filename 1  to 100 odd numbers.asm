.MODEL SMALL
.STACK 100H
.DATA
LF EQU '0DH'

.CODE
MAIN PROC
   MOV CX,0001H
   MOV DX,0
BACK: 
   MOV DL, CH
   OR DL, 30H
   MOV AH, 2
   INT 21H
   MOV DL, CL
   OR DL, 30H
   MOV AH, 2
   INT 21H
   CMP CL, 9
   MOV DL, 20H
   MOV AH, 2
   INT 21H
   JGE UNITSREACHED
   ADD CL, 2
   JMP UNITSUNREACHED
UNITSREACHED:
   ADD CH, 1
   MOV CL, 1
   CMP CH , 9
   JG EXIT
UNITSUNREACHED:
   JMP BACK
EXIT:
END MAIN 
MAIN ENDP