.MODEL SMALL
.STACK 100H

.DATA

.CODE

MAIN PROC
    MOV BL,0
    
    MOV AH,1
    INT 21H
    MOV BL,AL
    
    MOV AH,2
    MOV DL,0DH
    INT 21H
    MOV DL,0AH
    INT 21H
    
    CMP BL,'a'
    JGE C_UPPER:
    ADD BL,20H
    JMP DISPLAY
    
C_UPPER:
    SUB BL,20H
    JMP DISPLAY
    
DISPLAY:
    MOV DL,BL
    INT 21H
    
    MOV AH,4CH
    INT 21H
MAIN ENDP
    END MAIN